            $(this).find('.icon').removeClass('icon-leave').addClass('icon-enter');
            $(this).siblings().removeClass('hover');
            $(this).addClass('hover');
            $('.IndexPrivilegeCard .right .item').removeClass('ing').eq($(this).index()).addClass('ing');
        }, function () {
            $(this).find('.icon').removeClass('icon-enter').addClass('icon-leave');
        });
    },
    methods: {
        start_() {
            if (this.swiper) {
                this.swiper.destroy(true);
            }
            // 下一帧重新挂载Swiper实例
            this.$nextTick(() => {
                var swiper = new Swiper(".IndexBannerSwiper", {
                    pagination: {
                        el: ".IndexBannerSwiper .swiper-pagination",
                        clickable: true,
                    },
                    followFinger: false,
                    loop: true,
                    effect: 'fade',
                    fadeEffect: {
                        crossFade: true,
                    },
                    autoplay: {
                        delay: 5000,
                        disableOnInteraction: false,
                        pauseOnMouseEnter: true,
                    },
                    on: {
                        init: function (swiper) {
                            let  slide = this.slides.eq(0);
                            slide.addClass('ani-slide');
                        },
                        transitionStart: function () {
                            for (let i = 0; i < this.slides.length; i++) {
                                let  slide = this.slides.eq(i);
                                slide.removeClass('ani-slide');
                            }
                        },
                        transitionEnd: function () {
                            let slide = this.slides.eq(this.activeIndex);
                            slide.addClass('ani-slide');

                        },
                    }
                });
            });
        }
    }
