    data() {
        return {
            swiper: null,
            slides: [],
            activeIndex: 0
        }
    },
    mounted() {
        $('.FunctionCard .item').hover(function () {
            $(this).find('.icon').removeClass('icon-leave').addClass('icon-enter');
        }, function () {
            $(this).find('.icon').removeClass('icon-enter').addClass('icon-leave');
        });
